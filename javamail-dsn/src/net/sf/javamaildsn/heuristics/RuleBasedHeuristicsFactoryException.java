package net.sf.javamaildsn.heuristics;

public class RuleBasedHeuristicsFactoryException extends Exception {
	private static final long serialVersionUID = -708413033920495132L;

	public RuleBasedHeuristicsFactoryException() {
		super();
	}

	public RuleBasedHeuristicsFactoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public RuleBasedHeuristicsFactoryException(String message) {
		super(message);
	}

	public RuleBasedHeuristicsFactoryException(Throwable cause) {
		super(cause);
	}
}
